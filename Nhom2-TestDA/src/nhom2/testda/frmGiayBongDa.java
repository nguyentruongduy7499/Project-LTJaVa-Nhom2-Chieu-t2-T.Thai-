    //Ham Khoi tao
    public Giay khoitao() {
        Giay giay = new Giay();
        giay.setTenGiay(txtTenGiay.getText());
        giay.setHangGiay(txtHangGiay.getText());
        giay.setGia(Double.parseDouble(txtGiaGiay.getText()));
        giay.setSoGiay(Integer.parseInt(txtSoGiay.getText()));
        return giay;
    }
    //Ham hien Thi
    public void hienThi() {
        DefaultTableModel model = (DefaultTableModel) tbGiay.getModel();
        model.setRowCount(0);
        for (Giay giay : list.getList()) {
          
            model.addRow(new Object[]{giay.getTenGiay(), giay.getHangGiay(), giay.getGia(), giay.getSoGiay()});
        }
    }
    public void ClearAll(){
        txtTenGiay.setText("");
        txtHangGiay.setText("");
        txtGiaGiay.setText("");
        txtSoGiay.setText("");
    }
